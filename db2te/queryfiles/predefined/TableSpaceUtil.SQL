SELECT TBSP_ID, TBSP_NAME, TBSP_STATE, 

   CASE 
     WHEN TBSP_TYPE = 'SMS' THEN -1
     ELSE TBSP_UTILIZATION_PERCENT
   END AS TBSP_UTILIZATION_PERCENT,
   
   TBSP_TYPE, TBSP_CONTENT_TYPE,
   TBSP_PAGE_SIZE, TBSP_PREFETCH_SIZE, TBSP_EXTENT_SIZE, TBSP_TOTAL_SIZE_KB, TBSP_FREE_SIZE_KB,
   TBSP_USABLE_SIZE_KB, TBSP_USED_SIZE_KB, TBSP_USING_AUTO_STORAGE, TBSP_AUTO_RESIZE_ENABLED, 
   TBSP_INCREASE_SIZE, TBSP_INCREASE_SIZE_PERCENT, TBSP_LAST_RESIZE_TIME, TBSP_LAST_RESIZE_FAILED, 
   TBSP_MAX_SIZE, TBSP_NUM_CONTAINERS, SNAPSHOT_TIMESTAMP, TBSP_CREATE_TIME, REMARKS 
FROM SYSIBMADM.TBSP_UTILIZATION  
